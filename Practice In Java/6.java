/*
 * initialization
 *  do{
 *  code to execute
 *  increment/decrement
 *  }while(condition);
 * 
 */
//(do...while loop) The condition is checked later
//(do...while)অবস্থা পরে চেক করা হয়


	public static void main(String[] args)        
	{
		int x=1;
		do 
		{
		System.out.println(x);
		x++;
		}
		while(x<=10);
	}

	